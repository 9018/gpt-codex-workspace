/**
 * goal-relay-artifact-writer.mjs — Real repair artifact writer for Goal Relay.
 *
 * Writes repair artifacts to disk as structured Markdown files in the
 * `gptplan/` directory. Each artifact is a complete repair plan with
 * context, metadata, and actionable items.
 *
 * @module goal-relay/artifact-writer
 */

import { mkdirSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";

/**
 * Create a production repair artifact writer.
 *
 * @param {object} [options]
 * @param {string} [options.baseDir] - Base directory for writing artifacts.
 *   Defaults to process.cwd(). If set to ":memory:" no files are written.
 * @param {boolean} [options.dryRun] - If true, return paths without writing.
 * @returns {object} { write({ run, summary, repair_artifact, decision }) -> path }
 */
export function createRepairArtifactWriter(options = {}) {
  const baseDir = options.baseDir || process.cwd();
  const dryRun = options.dryRun === true;
  const inMemory = baseDir === ":memory:";

  /**
   * Write a repair artifact to disk.
   *
   * @param {object} opts
   * @param {object} opts.run - The execution run
   * @param {string} [opts.summary] - Summary of remaining work
   * @param {object} opts.repair_artifact - The artifact object from the relay service
   * @param {object} [opts.decision] - The relay decision context
   * @returns {Promise<string>} The absolute path of the written file
   */
  async function write({ run, summary = "", repair_artifact, decision } = {}) {
    if (!repair_artifact?.path) {
      throw new Error("repair_artifact.path is required");
    }

    const artifactPath = join(baseDir, repair_artifact.path);
    const dir = dirname(artifactPath);

    if (!inMemory && !dryRun) {
      if (!existsSync(dir)) {
        mkdirSync(dir, { recursive: true });
      }
    }

    const content = buildRepairArtifactContent({
      run,
      summary,
      repair_artifact,
      decision,
    });

    if (!inMemory && !dryRun) {
      writeFileSync(artifactPath, content, "utf-8");
    }

    return artifactPath;
  }

  return { write };
}

/**
 * Build the structured Markdown content for a repair artifact.
 *
 * @param {object} opts
 * @param {object} opts.run
 * @param {string} opts.summary
 * @param {object} opts.repair_artifact
 * @param {object} [opts.decision]
 * @returns {string} Markdown content
 */
function buildRepairArtifactContent({ run, summary, repair_artifact, decision } = {}) {
  const goalNumber = repair_artifact.goal_number || "?";
  const previousGoalId = repair_artifact.previous_goal_id || run?.goal_id || "unknown";
  const lines = [
    `# Repair Plan: Goal ${String(goalNumber).padStart(2, "0")}`,
    "",
    `_Auto-generated repair artifact for cycle ${goalNumber}._`,
    "",
    "---",
    "",
    "## Summary",
    "",
    summary || "Continue remaining work from previous iteration.",
    "",
    "## Metadata",
    "",
    `- **Goal Number**: ${goalNumber}`,
    `- **Previous Goal ID**: ${previousGoalId}`,
    `- **Root Goal ID**: ${repair_artifact.root_goal_id || run?.supervision?.goal_relay?.root_goal_id || previousGoalId}`,
    `- **Repair Artifact ID**: ${repair_artifact.id || "unknown"}`,
    `- **Created At**: ${repair_artifact.created_at || new Date().toISOString()}`,
    "",
    "## Context",
    "",
    `Previous goal \`${previousGoalId}\` completed with remaining work.`,
    "This artifact captures the scope still needing attention.",
    "",
    "## Remaining Work",
    "",
    (decision?.reason ? `- ${decision.reason}` : "- Continue and complete remaining objectives."),
    "",
    "## Next Steps",
    "",
    "1. Review the repair artifact content above.",
    "2. Create a new `/goal` with focus on the remaining work.",
    "3. Execute and verify the completion of all items.",
    "",
    "---",
    "",
    "_Generated by Goal Relay service._",
    "",
  ];

  return lines.join("\n");
}
